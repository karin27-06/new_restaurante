<template>
    <Head title="Tipo Empleado" />
    <AppLayout>
        <div>
            <template v-if="isLoading">
                <Espera/>
            </template>

            <template v-else>
                <div class="card">
                    <AddTipoEmpleado @tipo-empleado-agregado="refrescarListado"/>
                    <ListTipoEmpleado :refresh="refreshKey"/>
                </div>
            </template>
        </div>
    </AppLayout>
</template>

<script setup lang="ts">
import { ref, onMounted } from 'vue';
import AppLayout from '@/layout/AppLayout.vue';
import { Head } from '@inertiajs/vue3';
import Espera from '@/components/Espera.vue';
import ListTipoEmpleado from './Desarrollo/ListTipoEmpleado.vue';
import AddTipoEmpleado from './Desarrollo/AddTipoEmpleado.vue';

const isLoading = ref(true);
const refreshKey = ref(0);

function refrescarListado() {
    refreshKey.value++;
}

onMounted(() => {
    setTimeout(() => {
        isLoading.value = false;
    }, 1000);
});
</script>
